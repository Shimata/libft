#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const uint8_t	*a = (const uint8_t *)s1;
	const uint8_t	*b = (const uint8_t *)s2;
	size_t			i;

	i = 0;
	while (i < n)
	{
		if (a[i] != b[i])
			return (a[i] - b[i]);
		i++;
	}
	return (0);
}
